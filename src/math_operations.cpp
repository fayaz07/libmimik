namespace MathLib {
    
    int Addition(int a, int b) {
        return a + b;
    }
    
    double Addition(double a, double b) {
        return a + b;
    }
    
    int Subtraction(int a, int b) {
        return a - b;
    }
    
    double Subtraction(double a, double b) {
        return a - b;
    }
    
} // namespace MathLib